    private void cargarAlertasATabla() {
    DefaultTableModel modelo = new DefaultTableModel();
    modelo.addColumn("ID");
    modelo.addColumn("Interesado");
    modelo.addColumn("Asunto");
    modelo.addColumn("Prioridad");
    modelo.addColumn("Fecha Ingreso");

    java.util.List<proyecto_eda_model.Expediente> pendientes = new java.util.ArrayList<>();

    NodoDobleExpediente actual = listaExpedientes.getCabeza();
    while (actual != null) {
        Expediente exp = actual.getExpediente();
        if (!exp.getEstado().equalsIgnoreCase("Finalizado")) {
            pendientes.add(exp);
        }
        actual = actual.siguiente;
    }

    // Ordenamos: primero por prioridad, luego por fecha de ingreso (ya definido en compareTo)
    pendientes.sort(null); // Usa compareTo del Expediente

    for (proyecto_eda_model.Expediente exp : pendientes) {
        modelo.addRow(new Object[]{
            exp.getIdUnico(),
            exp.getInteresado().getNombres(),
            exp.getAsunto(),
            getPrioridadTexto(exp.getPrioridad()),
            exp.getFechaInicio().toLocalDate().toString()
        });
    }

    tablaAlertas.setModel(modelo);
}
    
    
    
    private String getPrioridadTexto(int prioridad) {
        return switch (prioridad) {
            case 1 -> "Alta";
            case 2 -> "Media";
            case 3 -> "Baja";
            default -> "Desconocida";
        };
    }
    
    
